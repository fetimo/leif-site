<h1 class="inline">Privacy</h1>

<h2>Notice</h2>
<p>Leif does not store any personal information about you. Leif does not track your activities nor make use of cookies.</p>

<h2>Access</h2>
<p>Leif stores the wattage data and a cache of API responses. This can be accessed at ~/Library/Containers/com.fetimo.leif. You are free to remove all data in here at any time.</p>

<h2>Security</h2>
<p>Leif uses HTTPS to securely get data about the current carbon mix in the UK from a third-party service called carbonintensity.org.uk run by National Grid. Leif has no association with the National Grid.</p>

<h2>Enforcement</h2>
<p>Leif is open-source so it is relatively easy to see if the code deviates from this privacy policy. If you believe that a clause has been violated or is out-of-date please raise an issue.</p>

