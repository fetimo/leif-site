  <details>
    <summary>Where does Leif collect it's data from?</summary>
    <p>Leif uses a variety of sources depending on the country you're in. For the UK it's <a href="https://carbonintensity.org.uk/" target="_blank">Carbon Intensity</a>. For other countries Leif uses <a href="https://github.com/Green-Software-Foundation/carbon-aware-sdk">Carbon Aware</a>.</p>
  </details>
  <details>
    <summary>Why does Leif always look angry?</summary>
    <p>Sad of seeing Leif upset? It's not good for her and it's not good for you! The only way to make Leif happy is to move to an area already utilising renewable energy or <a href="/mp">get in touch with your representative</a> to enact change.</p>
  </details>
  <details>
    <summary>What do you do with the data?</summary>
    <p>Nothing! We don't collect any data. All of the carbon data and usage data lives on your computer.</p>
  </details>
  <details>
    <summary>Something isn't working, what can I do?</summary>
    <p>Sorry to hear that! Thanks for making the effort to get in touch, you can either <a href="https://github.com/leif-app/leif-desktop/issues/new">open an issue</a> or <a href="mailto:hello@leif.support">email me</a></p>

  </details>
